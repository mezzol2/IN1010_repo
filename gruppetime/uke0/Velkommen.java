class Velkommen{
    public static void main(String[] args) {
        String name = "Austin";
        //for loop
        for (int i = 1; i < 11; i++){
            System.out.println("Velkommen "+name+" for " +i+". gang!");
        }
        //while loop
        int n = 1;
        while (n < 11){
            System.out.println("Velkommen "+name+" for " +n+". gang!");
            n++;
        }
    }
}